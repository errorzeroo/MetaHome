DROP TABLE IF EXISTS AI_LEARN;

CREATE TABLE AI_LEARN(
ADDRESS VARCHAR2(1000) PRIMARY KEY,
PARK_NAME VARCHAR2(1000) UNIQUE,
PARK_DIST INTEGER NOT NULL,
HOS_NAME VARCHAR2(1000) UNIQUE,
HOS_DIST INTEGER NOT NULL,
SUB_NAME VARCHAR2(1000) UNIQUE,
SUB_DIST INTEGER NOT NULL,
BUS_NAME VARCHAR2(1000) UNIQUE,
BUS_DIST INTEGER NOT NULL,
PARKING_NAME VARCHAR2(1000) UNIQUE,
PARKING_DIST INTEGER,
LOW_NAME VARCHAR2(1000) UNIQUE,
LOW_DIST INTEGER NOT NULL,
MID_NAME VARCHAR2(1000) UNIQUE,
MID_DIST INTEGER NOT NULL,
HI_NAME VARCHAR2(1000) UNIQUE,
HI_DIST INTEGER,
CF_NAME VARCHAR2(1000) UNIQUE,
CF_DIST INTEGER,
CONVE_NAME VARCHAR2(1000) UNIQUE,
CONVE_DIST INTEGER,
ART_NAME VARCHAR2(1000) UNIQUE,
ART_DIST INTEGER);